package com.example.practic.theme_1_kotlin.test

/**
2. Что мы увидим в консоли при вызове этой функции?


Объяснение:
Что происходит в функции test?

Мы проходимся по списку listOf(1, 2, 3) с помощью forEach.
Если текущий элемент (it) равен 2, вызывается return@test.
Что делает return@test?

Метка test@ используется, чтобы указать, что выход должен произойти только из текущей итерации forEach (локальный выход).
Это эквивалентно вызову continue в цикле for.
Разбор по шагам:

Элемент 1: Условие if (it == 2) ложно. Выполняется print("$it, "). В консоль выводится 1, .
Элемент 2: Условие if (it == 2) истинно. Вызывается return@test, итерация завершается, и код после этого не выполняется.
Элемент 3: Условие if (it == 2) ложно. Выполняется print("$it, "). В консоль выводится 3, .
Итог:

Элементы 1 и 3 обрабатываются и выводятся в консоль.
Элемент 2 пропускается из-за return@test.*/

fun test() {
    listOf(1, 2, 3).forEach test@{
        if (it == 2) return@test

        print("$it, ")
    }
}

fun main() {
    test()
}
